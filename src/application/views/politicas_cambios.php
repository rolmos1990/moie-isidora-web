    <h1>POLÍTICAS DE CAMBIO</h1>
    
    <div class="pregunta">
       <p>
           Si deseas hacer el cambio de alguno de nuestros productos, debes
            tener en cuenta que:
       </p>
       <ul>
           <li>Las prendas en promoción o con descuentos NO tienen cambio.</li>
           <li>Los cambios de las prendas siempre se realizarán por el valor que el
cliente pagó.</li>
           <li>Accesorios, Ropa Interior y Cosméticos no tienen cambio.</li>
           <li>La prendas adquiridas de la sección de <b>TODO A $17.000</b> presentan
alguna de las siguientes condiciones: reparadas, re manufacturadas y
descontinuadas, por lo tanto la garantía legal no será exigible.</li>
           <li>Es importante que el producto no debe estar modificado o alterado
de su estado original y la prenda debe estar en buen estado y limpia y
con sus etiquetas originales.</li>
        <li>Los productos no pueden estar usados para poder hacer efectivo el
        cambio.</li>
       </ul>
       <p>Una vez hayas solicitado la devolución de tu producto podrás escoger
entre las siguientes opciones:</p>
<ul>
    <li>Nota crédito para realizar una nueva compra en isidoramodas.com.</li>
    <li>Devolución del dinero.</li>
</ul>
<p>
    Una vez radicada la solicitud de cambio con los datos necesarios para
la misma la operadora de ventas le indicará por los canales de
atención por donde se realizó la compra la empresa transportadora por

donde debe enviarse la (las) prendas. Puedes utilizar el mismo
empaque en que recibiste el pedido o puedes usar uno de tu
preferencia, sin embargo, es importante que el empaque sea el
adecuado según la naturaleza del producto para que este no se pueda
ver afectado durante el proceso de transporte.
</p>
<p>
    Es importante que tengas en cuenta que: el producto no debe estar
modificado o alterado de su estado original y la prenda debe estar en
buen estado y limpia y con sus etiquetas originales.
</p>
<h4>Los tiempos para el cambio:</h4>
<ul>
    <li>La nota crédito para realizar una nueva compra en isidoramodas.com
será asentada de forma inmediata en nuestro sistema y será redimible
en cualquier momento por parte de la cliente una vez que se verifique
la recepción de las prendas objeto del cambio en nuestras bodegas y
que las mismas cumplan con las condiciones necesarias.</li>
</ul>
<h4>Devolución del dinero</h4>
<ul>
<li>A través de transferencia: se realiza aproximadamente dentro de los
8 días hábiles siguientes de recibir el producto en nuestra bodega.</li>
</ul>
<p>Verificadas las condiciones en que se encuentra el producto, <b>LUCY
MODAS COLOMBIA S.A.S</b> tendrá 3 días hábiles para informarte si tu
producto cumple con las con los criterios aquí establecidos para hacer
el cambio por talla o color, por nota de crédito para otro producto o
devolución del dinero (solo aplica para reclamos por garantía o
derecho de retracto).</p>
<h4>¿Cuándo los productos NO cumplen con las políticas de cambios
y garantías?</h4>
<p>Tu producto NO cumple con los criterios de nuestra política de
cambios y garantías sí:</p>
<ul>
    <li>El producto está modificado o alterado de su estado original.</li>
    <li>La prenda está deteriorada por uso o lavado.</li>
    <li>Accesorios, ropa interior y cosméticos no tienen cambio, ni las
prendas de nuestra sección de <b>TODO A $17.000.</b></li>
</ul>
<p>Te informaremos con un correo electrónico la razón por la cual no
procede el cambio o la garantía y el producto será devuelto a la
dirección que te suministre la operadora de ventas.</p>
<h4>Derecho de Retracto:</h4>
<p>
    De acuerdo con el derecho de retracto consagrado por la ley 1480 de
2011 art. 47, el término máximo para ejercer este derecho, es de 5
días hábiles contados a partir de la entrega del producto.
</p>
<p>El producto deberá ser devuelto en las mismas condiciones en que lo
recibiste.</p>
<p>Los costos de transporte y demás, incurridos en la devolución serán
asumidos por ti.</p>
<p>Para gestionar la devolución de tu producto te debes comunicar por
nuestros canales de atención en isidoramodas.com, Facebook o
Whatsapp.</p>
<p>En caso de que se cumplan todas las condiciones mencionadas,
<b>ISIDORA MODAS S.A.S</b> devolverá la suma total pagada, sin
descuentos o retenciones, y el término para la devolución del dinero
no podrá exceder de 30 días calendario.</p>
<p>De acuerdo con el numeral 7 del artículo 47 de la ley 1480, se
exceptúan del derecho de retracto los bienes de uso personal.</p>
